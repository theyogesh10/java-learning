package com.company;

public class Java_002_Literals {

    public static void main(String [] args){

        // Integral Primitive Data Type
        byte age = 25;
        int age2 = 34;
        short age3 = 78;
        long age4 = 5673456783456L;

        // Floats/Decimal Primitive Data Type
        float f1 = 5.6f;
        double d1 = 4.66D;
        double d2 = 9.87;

        // Char Primitive Data Type
        char ch = 'Y';

        // Boolean Primitive Data Type
        boolean a = true;

        System.out.println(age);

        // String Non-Primitive Data Type
        String str = "Madhya Pradesh";
        System.out.println(str);

    }


}
